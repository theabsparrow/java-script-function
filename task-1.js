function multiplyNumber(num1, num2, num3, num4) {
    let multiplicationNum = num1 * num2 * num3 * num4;
    return multiplicationNum;
}

let firstNum = 10;
let secondNum = 5;
let thirdNum = 12;
let fourthNum = 14;
const totalNum = multiplyNumber(firstNum, secondNum, thirdNum, fourthNum);
console.log(totalNum);
